void main() {
  //Criação de uma variável: tipo nomedavariavel = valor - Ex: int num = 3;
  int numero = 3;

  //Printar só a variável: print(variavel) - Ex: print(num);
  print(numero);

  //Printar uma variável em com um texto: print("Texto: $variavel") - Ex: print("Número: $num"); 
  print("Número: $numero");
 }